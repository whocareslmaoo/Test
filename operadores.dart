import 'dart:math';

void main() {

  double nota1= 10;
  double nota2= 10;

  print(nota1+nota2);
  print(nota1-nota2);
  print(nota1*nota2);
  print(nota1/nota2);
  print(nota1~/ nota2); //retorna apenas a parte inteira
  print(nota1%nota2);           

  print(nota1 > nota2);
  print(nota1 >= nota2);
  print(nota1 < nota2);
  print(nota1 <= nota2);
  print(nota1 == nota2);
  print(nota1 != nota2);
  
}